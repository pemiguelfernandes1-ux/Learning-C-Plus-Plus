#include <iostream> /*
Imports the default library "<iostream>" for the compiler. This library enable the compiler
how to use the keyboard and monitor.
*/

int main() { // "int main()" is the keyword that the computer looks for to start the program.
    std::cout << "Hello, World!" << std::endl; // It tells the pc what to display on the screen.
    return 0; // This tells the computer that the program returned successfully.
}